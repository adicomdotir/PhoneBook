    public EditJFrame(int index) {
        this();
        gIndex = index;
        String[] strArr = MyIO.readFile();
        String[] temp = new String[5];
        int k = index;
        strArr[k] = "," + strArr[k];
        int ii = strArr[k].length();
        int j = 0;
        for(int i=strArr[k].length()-1; i>=0; i--) {
            if(strArr[k].charAt(i)==',') {
                temp[j] = strArr[k].substring(i+1, ii);
                ii = i;
                j++;
            }
        }
        jTextNumber.setText(temp[4]);
        jTextName.setText(temp[3]);
        jTextLastName.setText(temp[2]);
        jTextPhone.setText(temp[1]);
        jTextAddress.setText(temp[0]);
    }

